export async function GET(request: NextRequest) {
  const secret = process.env.MIGRATE_SECRET
  const provided = new URL(request.url).searchParams.get('secret')
  if (!secret || provided !== secret) {
    return NextResponse.json({ error: 'Forbidden' }, { status: 403 })
  }

  const details: Array<{ old: string; new: string; totalMoved: number; msgKeysMoved: number }> = []
  let totalViews = 0
  let totalMsgKeys = 0

  for (const [oldAddr, newAddr] of PAIRS) {
    let totalMoved = 0
    let msgKeysMoved = 0

    const total = await kv.get<number>(`views:total:${oldAddr}`)
    if (total && total > 0) {
      await kv.incrby(`views:total:${newAddr}`, total)
      totalMoved = total
      totalViews += total
    }

    let cursor = 0
    do {
      const [nextCursor, keys] = await kv.scan(cursor, { match: `views:msg:${oldAddr}:*`, count: 100 })
      cursor = nextCursor as number
      for (const key of keys) {
        const value = await kv.get<number>(key)
        if (value && value > 0) {
          const hash = key.split(':').pop()!
          await kv.incrby(`views:msg:${newAddr}:${hash}`, value)
          msgKeysMoved++
          totalMsgKeys++
        }
      }
    } while (cursor !== 0)

    if (totalMoved > 0 || msgKeysMoved > 0) {
      details.push({ old: oldAddr, new: newAddr, totalMoved, msgKeysMoved })
    }
  }

  return NextResponse.json({
    ok: true,
    pairsChecked: PAIRS.length,
    pairsWithViews: details.length,
    totalViewsMigrated: totalViews,
    totalMsgKeysMigrated: totalMsgKeys,
    details,
  })
}
