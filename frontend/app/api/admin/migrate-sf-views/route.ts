export async function POST(req: NextRequest) {
  const secret = req.headers.get('x-admin-secret')
  if (secret !== process.env.ADMIN_SECRET) {
    return NextResponse.json({ error: 'Unauthorized' }, { status: 401 })
  }

  let copied = 0
  let skipped = 0
  let missing = 0

  for (const [oldAddr, newAddr] of Object.entries(OLD_TO_NEW)) {
    const oldKey = `views:total:${oldAddr}`
    const newKey = `views:total:${newAddr}`

    const [oldVal, newVal] = await Promise.all([kv.get<number>(oldKey), kv.get<number>(newKey)])

    if (!oldVal) {
      missing++
      continue
    }
    if (newVal) {
      skipped++
      continue
    }

    await kv.set(newKey, oldVal)
    copied++
  }

  return NextResponse.json({ ok: true, copied, skipped, missing, total: Object.keys(OLD_TO_NEW).length })
}
